/// Strings para la página de confirmación de eliminación.
class DeleteConfirmationStrings {
  const DeleteConfirmationStrings({
    required this.title,
    required this.warningIrreversible,
    required this.warningLoseAccess,
    required this.warningRegulatedData,
    required this.deleteButtonLabel,
    required this.cancelButtonLabel,
    required this.modalTitle,
    required this.modalMessage,
    required this.modalConfirmLabel,
    required this.modalCancelLabel,
  });

  final String title;
  final String warningIrreversible;
  final String warningLoseAccess;
  final String warningRegulatedData;
  final String deleteButtonLabel;
  final String cancelButtonLabel;
  final String modalTitle;
  final String modalMessage;
  final String modalConfirmLabel;
  final String modalCancelLabel;
}
